/* ── Detecció automàtica ── */
function detectLang() {
  const saved = localStorage.getItem("dbd-lang");
  if (saved && TRANSLATIONS[saved]) return saved;
  const browser = (navigator.language || navigator.userLanguage || "ca").slice(0, 2).toLowerCase();
  return TRANSLATIONS[browser] ? browser : "ca";
}

/* ── Exposa globalment ── */
window.TRANSLATIONS = TRANSLATIONS;
window.detectLang = detectLang;
